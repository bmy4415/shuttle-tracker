class _DriverHomeScreenState extends State<DriverHomeScreen> with TickerProviderStateMixin {
  bool _isTracking = false;
  final LocationService _locationService = LocationService();
  final ParentLocationService _parentLocationService = ParentLocationService();
  LocationData? _currentLocation;
  String? _statusMessage;
  bool _isLoadingLocation = false;
  late AnimationController _progressController;
  late Animation<double> _progressAnimation;
  
  // ÌïôÎ∂ÄÎ™® ÏúÑÏπò Ï∂îÏ†Å Í¥ÄÎ†®
  List<ParentData> _parentLocations = [];
  bool _isTrackingParents = false;

  @override
  void initState() {
    super.initState();
    _progressController = AnimationController(
      duration: const Duration(seconds: 8), // ÏòàÏÉÅ ÏÜåÏöîÏãúÍ∞Ñ 8Ï¥à
      vsync: this,
    );
    _progressAnimation = Tween<double>(begin: 0.0, end: 1.0).animate(
      CurvedAnimation(parent: _progressController, curve: Curves.easeInOut),
    );
    
    // ÌïôÎ∂ÄÎ™® ÏúÑÏπò Ïä§Ìä∏Î¶º Î¶¨Ïä§ÎÑà ÏÑ§Ï†ï
    _parentLocationService.parentLocationStream.listen((parents) {
      if (mounted) {
        setState(() {
          _parentLocations = parents;
        });
      }
    });
  }

  @override
  void dispose() {
    _progressController.dispose();
    _locationService.dispose();
    _parentLocationService.dispose();
    super.dispose();
  }

  Future<void> _toggleTracking() async {
    if (_isTracking) {
      _locationService.stopLocationTracking();
      _parentLocationService.stopTrackingParents(); // ÌïôÎ∂ÄÎ™® ÏúÑÏπò Ï∂îÏ†ÅÎèÑ Ï§ëÏßÄ
      setState(() {
        _isTracking = false;
        _isTrackingParents = false;
        _statusMessage = 'ÏúÑÏπò Ï†ÑÏÜ° Ï§ëÏßÄÎê®';
      });
    } else {
      setState(() {
        _statusMessage = 'ÏúÑÏπò Í∂åÌïú ÌôïÏù∏ Ï§ë...';
      });

      bool started = await _locationService.startLocationTracking(
        busId: 'BUS001',
        driverId: 'DRIVER001',
      );

      if (started) {
        // ÌïôÎ∂ÄÎ™® ÏúÑÏπò Ï∂îÏ†ÅÎèÑ ÏãúÏûë (ÏãúÎÆ¨Î†àÏù¥ÏÖò)
        setState(() {
          _isTrackingParents = true;
        });

        _locationService.locationStream.listen((LocationData location) {
          setState(() {
            _currentLocation = location;
            _statusMessage = 'ÏúÑÏπò Ï†ÑÏÜ° Ï§ë (${location.latitude.toStringAsFixed(4)}, ${location.longitude.toStringAsFixed(4)})';

            // Í∏∞ÏÇ¨ ÏúÑÏπòÎ•º Í∏∞Ï§ÄÏúºÎ°ú ÏãúÎÆ¨Î†àÏù¥ÏÖò ÌïôÎ∂ÄÎ™® ÏúÑÏπò ÏÉùÏÑ±
            _parentLocations = _locationService.getSimulatedParentLocations(location);
          });
        });

        setState(() {
          _isTracking = true;
        });
      } else {
        setState(() {
          _statusMessage = 'ÏúÑÏπò Í∂åÌïúÏù¥ ÌïÑÏöîÌï©ÎãàÎã§. ÏÑ§Ï†ïÏóêÏÑú ÏúÑÏπò Í∂åÌïúÏùÑ ÌóàÏö©Ìï¥Ï£ºÏÑ∏Ïöî.';
        });
      }
    }
  }

  Future<void> _getCurrentLocation() async {
    if (_isLoadingLocation || !mounted) return; // Ï§ëÎ≥µ ÌÅ¥Î¶≠ Î∞©ÏßÄ + mounted Ï≤¥ÌÅ¨

    setState(() {
      _isLoadingLocation = true;
      _statusMessage = 'ÌòÑÏû¨ ÏúÑÏπò Í∞ÄÏ†∏Ïò§Îäî Ï§ë...';
